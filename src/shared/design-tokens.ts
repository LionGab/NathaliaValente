// Type definitions
export type DesignTokens = typeof tokens;
export type ColorScale = keyof typeof tokens.colors.primary;
export type SpacingScale = keyof typeof tokens.spacing;
export type FontSizeScale = keyof typeof tokens.typography.fontSize;
export type FontWeightScale = keyof typeof tokens.typography.fontWeight;

// Utility functions
export const getColor = (color: string, scale: ColorScale = '500') => {
  const [category, ...rest] = color.split('-');
  const colorName = rest.join('-') as keyof typeof tokens.colors;
  return tokens.colors[colorName]?.[scale] || color;
};

export const getSpacing = (scale: SpacingScale) => {
  return tokens.spacing[scale];
};

export const getFontSize = (scale: FontSizeScale) => {
  return tokens.typography.fontSize[scale];
};

// WCAG Contrast validation
export const validateContrast = (foreground: string, background: string): number => {
  // Simplified contrast calculation - in production, use a proper library
  const getLuminance = (hex: string) => {
    const rgb = parseInt(hex.slice(1), 16);
    const r = (rgb >> 16) & 0xff;
    const g = (rgb >> 8) & 0xff;
    const b = (rgb >> 0) & 0xff;
    const [rs, gs, bs] = [r, g, b].map(c => {
      c = c / 255;
      return c <= 0.03928 ? c / 12.92 : Math.pow((c + 0.055) / 1.055, 2.4);
    });
    return 0.2126 * rs + 0.7152 * gs + 0.0722 * bs;
  };

  const l1 = getLuminance(foreground);
  const l2 = getLuminance(background);
  const lighter = Math.max(l1, l2);
  const darker = Math.min(l1, l2);
  
  return (lighter + 0.05) / (darker + 0.05);
};

